package lesson2;

public class Literals {

    public static void main(String[] args) {
        System.out.println("Некоторые виды литералов:");
        System.out.println("а)логический литерал - " + false);
        System.out.println("б)строковй - " + "Литерал (англ. literal ) — запись в исходном коде компьютерной программы, представляющая собой фиксированное значение.");
        System.out.println("в)символьный - " + '\u26c5');
        System.out.println("г)целочисленный 2-й - " + 0b01001100);
        System.out.println("д)целочисленный 8-й - " + 012);
        System.out.println("е)целочисленный 10-й - " + 451);
        System.out.println("ж)целочисленный 16-й - " + 0xAB);
        System.out.println("з)литерал типа float - " + 3.1415926535F);
        System.out.println("и)литерал типа double - " + 1.38e-23);
    }
}
